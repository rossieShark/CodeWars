func squareDigits(_ num: Int) -> Int {
  let strNum = String(num)
    var result = strNum.compactMap { Int("\($0)") }.map {"\($0 * $0)"}.joined()
    
  return Int(result) ?? 0
}

func squareDigits1(_ num: Int) -> Int {
    var a = num
    var result = ""
    while a > 0 {
        var remainder = a % 10
        result += String(remainder * remainder)
        a /= 10
    }
    return Int(result) ?? 0
}
