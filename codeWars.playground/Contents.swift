func isPalindrom(_ str: String) -> Bool {
    var reverse = ""
    var lowercased = str.lowercased().filter{ ($0 == " ") }
    for i in lowercased {
        var strValue = "\(i)"
        reverse = strValue + reverse
        }
        
    return reverse == lowercased
    }
