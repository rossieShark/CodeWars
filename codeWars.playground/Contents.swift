func isIdentical2(str1: String, str2: String) -> Bool {
    var checkString = str2
    for i in str1 {
        if let index = checkString.firstIndex(of: i) {
            checkString.remove(at: index)
        }
    }
    return checkString.count == 0
}
    
    isIdentical2(str1: "Helllo", str2: "lohle")

