func isIdentical(str1: String, str2: String) -> Bool {
    var flag = false
    for i in str1 {
        if str2.contains(i) && str1.count == str2.count {
            flag = true
        }
    }
    return flag
}
    
    isIdentical(str1: "Helllo", str2: "lohle")

